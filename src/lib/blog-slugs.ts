/**
 * Get localized blog slug.
 * If no translation exists, returns the original (PL) slug.
 */
export function getLocalizedBlogSlug(plSlug: string, locale: string): string {
  return BLOG_SLUG_MAP[plSlug]?.[locale] ?? plSlug;
}

/**
 * Reverse lookup: given a localized slug, find the PL canonical slug.
 */
export function getCanonicalBlogSlug(localizedSlug: string, locale: string): string | null {
  for (const [plSlug, translations] of Object.entries(BLOG_SLUG_MAP)) {
    if (translations[locale] === localizedSlug || plSlug === localizedSlug) {
      return plSlug;
    }
  }
  return null;
}
